export function getWordPoints(word: string, startX: number, startY: number, canvasWidth: number = 800, canvasHeight: number = 600): number[][] {
  const normalizedWord = word.toLowerCase().trim();
  
  // Check if we have a specific drawing for this word
  if (BOT_DRAWINGS[normalizedWord]) {
    const drawing = BOT_DRAWINGS[normalizedWord];
    const scale = Math.min(canvasWidth, canvasHeight) * 0.6;
    const offsetX = (canvasWidth - scale) / 2;
    const offsetY = (canvasHeight - scale) / 2;
    
    return drawing.map(line => {
      const points: number[] = [];
      for (let i = 0; i < line.length; i += 2) {
        points.push(offsetX + (line[i] / 100) * scale);
        points.push(offsetY + (line[i + 1] / 100) * scale);
      }
      return points;
    });
  }

  // Fallback: Draw a generic "box with a question mark" or something if we don't want spelling
  // But for now, let's keep spelling as a last resort but make it look more like a "drawing"
  const allLines: number[][] = [];
  let currentX = startX;
  const scale = 30;
  
  for (const char of normalizedWord) {
    const charData = BOT_FONT[char] || BOT_FONT['.'];
    for (const line of charData) {
      const points: number[] = [];
      for (let i = 0; i < line.length; i += 2) {
        points.push(currentX + line[i] * (scale / 10));
        points.push(startY + line[i + 1] * (scale / 10));
      }
      allLines.push(points);
    }
    currentX += scale * 1.2;
  }
  
  return allLines;
}
